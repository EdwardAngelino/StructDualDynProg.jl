@testset "Comparison" begin
    @test SOI._isapproxzero(0, 1e-10)
    @test !SOI._isapproxzero(1, 1e-10)
    @test SOI._isapproxzero(1e-5, 1e-4)
    @test !SOI._isapproxzero(1e-3, 1e-4)
    @test SOI._isapproxzero([1e-5, 1e-5], 1e-4)
    @test !SOI._isapproxzero([1e-5, 1e-3], 1e-4)
    @test SOI._isapprox(0, 0, 1e-3)
    @test SOI._isapprox(1, 1, 1e-3)
    @test !SOI._isapprox(0, 1, 1e-3)
    @test SOI._lt(0, 1, 1e-3)
    @test !SOI._lt(1, 1, 1e-3)
    @test !SOI._lt(1, 0, 1e-3)
    @test !SOI._lt(1, BigInt(1), 1e-3)
end
